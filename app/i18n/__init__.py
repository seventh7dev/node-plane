def normalize_locale(locale: str | None) -> str:
    if not locale:
        return DEFAULT_LOCALE
    locale = locale.strip().lower()
    if locale.startswith("en"):
        return "en"
    if locale.startswith("ru"):
        return "ru"
    return DEFAULT_LOCALE


def t(locale: str | None, key: str, **kwargs: Any) -> str:
    lang = normalize_locale(locale)
    text = TRANSLATIONS.get(lang, {}).get(key) or TRANSLATIONS[DEFAULT_LOCALE].get(key) or key
    return text.format(**kwargs) if kwargs else text


def detect_locale(update: Update | None) -> str:
    user = update.effective_user if update else None
    language_code = getattr(user, "language_code", None)
    return normalize_locale(language_code)


def get_user_locale(user_id: int | None, fallback: str | None = None) -> str:
    if user_id is None:
        return normalize_locale(fallback)
    db = user_store.read()
    rec = db.get(str(user_id)) if isinstance(db, dict) else None
    if isinstance(rec, dict):
        return normalize_locale(rec.get("locale") or fallback)
    return normalize_locale(fallback)


def get_locale_for_update(update: Update | None) -> str:
    user_id = update.effective_user.id if update and update.effective_user else None
    return get_user_locale(user_id, detect_locale(update))


def set_user_locale(user_id: int, locale: str) -> str:
    normalized = normalize_locale(locale)
    user_store.upsert_user(user_id, locale=normalized, locale_explicitly_selected=True)
    return normalized
